//Arrow function
const multiply = (num1, num2, num3) => num1 * num2 * num3;
console.log(multiply(2, 3, 4));


// Multiline String
const threeLines = `I am a web developer.
I love to code.
I love biriyani.`;
console.log(threeLines);


// Default value
const sum = (n1, n2 = 20) => n1 + n2;
console.log(sum(10));